<h1><strong>Keirsey and Bartle</strong></h1>
<p>The first of the two major assertions I make in this article is that the four temperaments described by David Keirsey -- Artisan, Guardian, Rational, and Idealist -- are supersets of the original four player types -- Killer, Achiever, Explorer, and Socializer, respectively -- as described by Richard Bartle.</p>
