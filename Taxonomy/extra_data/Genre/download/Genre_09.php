<p>In fact, several of the best-known play style and game design models share many conceptual elements. So I'm also proposing here that the Bartle typology, the play style models of Caillois, Lazzaro, and Bateman, and the game design models of Edwards and Hunicke/LeBlanc/Zubek are all variations on a single Unified Model of play styles.</p>
<p>(<em>Please note that any and all references I make in this article to the works of Richard Bartle, David Keirsey, Christopher Bateman and others that aren't clearly sourced as quotations are my own interpretations. As such, they should not be considered official descriptions of these authors' ideas</em>.)</p>
<h1><strong>The Four Bartle Types</strong></h1>
<p>The official description of the original four Bartle Types (which have been expanded to eight types in Richard Bartle's book Designing Virtual Worlds) is preserved in the paper "<span style="text-decoration: underline;"><a href="http://www.mud.co.uk/richard/hcds.htm">Hearts, Clubs, Diamonds, Spades: Players Who Suit MUDs</a></span>" by Multi-User Dungeon (MUD) co-creator Richard Bartle.</p>
<p>This model, which was based on observing and analyzing the behaviors people playing together in a multi-user game, holds that there are four different kinds of play style interests, each of which is given a descriptive name: Killers, Achievers, Explorers, and Socializers.</p>
<ul>
<li><strong>Killers</strong>: interfere with the functioning of the game world or the play experience of other players</li>
<li><strong>Achievers</strong>: accumulate status tokens by beating the rules-based challenges of the game world</li>
<li><strong>Explorers</strong>: discover the systems governing the operation of the game world</li>
<li><strong>Socializers</strong>: form relationships with other players by telling stories within the game world</li>
</ul>
<p>These four styles emerged from the combination of two primary gameplay interests, which I've called Content and Control, each of which has two mutually exclusive forms. Content is defined to mean either <strong>acting</strong> simply and directly on objects in the game world, or <strong>interacting</strong> more deeply with world-systems. Control refers to how players want to experience the game world -- either through the dynamic behaviors of other players, or with the relatively static world of the game itself.</p>
<p>Killers and Achievers both turned out to be mostly interested in acting on things or people, treating things and people as external objects. At the same time, Explorers and Socializers both seemed to prefer a deeper level of interacting with things or other people, focusing on internal qualities.</p>
<p>Similarly, Killers and Socializers both seemed eager to have the opportunity to control how they are able to play dynamically with others in the game world, while Achievers and Explorers seemed most interested in controlling their relationships with the developer-defined objects in and properties of the game world itself.</p>
<p>The bases of the Bartle Types are thus two pairs of complementary player goals: Acting or Interacting (content), and Players or World (control). Bartle represented these interests as two lines at right angles to each other to create a grid with four quadrants, each quadrant corresponding to one of the four observed play style preferences. By determining his preference for Acting vs. Interacting and for Players vs. World, then looking up the play style in the quadrant corresponding to that combination, any gamer could easily identify his naturally preferred play style. A gamer who prefers acting over interacting and is focused more on the world of the game than other players, for example, would most likely demonstrate Achiever behaviors when playing a game.</p>
<p>Here's a diagram showing how the four Bartle Types emerge from the conjunction of the two major gamer concerns with content and control. (Note: This table is rotated 90 degrees clockwise from the version presented in "Players Who Suit MUDs" for reasons that will become apparent later in this article.)</p>
<p style="text-align: center;"><img style="margin: 5px; border: 1px solid black;" src="/db_area/images/feature/6474/03-UM-BartleTypes.png" alt="" width="576" height="432" /><strong><br />The   Bartle Types</strong></p>
<h1><strong>The Four Keirsey Temperaments</strong></h1>
<p>In the 1970s, psychologist David Keirsey identified <a href="http://www.keirsey.com/"><span style="text-decoration: underline;">four general patterns</span></a> from the sixteen types of the Myers-Briggs personality model. In his book (co-written with Marilyn Bates) Please Understand Me, Keirsey described these four "temperaments," giving them descriptive names much as Richard Bartle named his player types:</p>
<ul>
<li><strong>Artisan</strong> (Sensing + Perceiving): realistic, tactical, manipulative (of things or people), pragmatic, impulsive, action-focused, sensation-seeking</li>
<li><strong>Guardian</strong> (Sensing + Judging): practical, logistical, hierarchical, organized, detail-oriented, possessive, process-focused, security-seeking</li>
<li><strong>Rational</strong> (iNtuition + Thinking): innovative, strategic, logical, scientific/technological, future-oriented, result-focused, knowledge-seeking</li>
<li><strong>Idealist</strong> (iNtuition + Feeling): imaginative, diplomatic, emotional, relationship-oriented, dramatic, person-focused, identity-seeking</li>
</ul>
<p>In the second edition of Keirsey's book, Please Understand Me II, Keirsey grouped his four temperaments as four quadrants across two axes to show how they were related according to an internal structure, very much as Richard Bartle had. However, by the time he proposed his grouping model in the second edition of his book, I had already worked out a somewhat different arrangement.</p>
<p>Rather than the two dimensions that Keirsey used in his model, I believe the two most fundamentally distinctive dimensions of human behavior are Internals (a preference for seeing possibilities and the abstract) vs. Externals (seeing the concrete and realistic), and Change (which can be thought of as freedom or opportunity) vs. Structure (which can be understood as rules or organization). Each of the four temperaments is thus a combination of External/Internal and Change/Structure:</p>
