<p>Where Bartle sees a preference for interacting with or acting on players in a game context, temperament theory sees a more general preference for internal or external change. And where Bartle focuses in a gameplay context on a preference for dynamic players or the static world, my version of Keirsey's four-quadrant model has people generally preferring change or structure. I believe that because the basic two-valued motivations are analogous between the Bartle Types and the Keirsey temperaments, the types and temperaments that are generated by these motivations are also analogous.</p>

