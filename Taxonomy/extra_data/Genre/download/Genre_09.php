                <p>[<em>In this comprehensive analysis, multiple psychological systems of gameplay are surveyed, to try and arrive at a unified model in which player behavior can be understood and, crucially for game developers, catered to</em>.]</p>
<p>Numerous models of gamer psychology have been proposed and debated over the past couple of decades. One of the earliest and simplest has proven to be one of the most referenced and most enduring: the <a href="http://www.mud.co.uk/richard/hcds.htm"><span style="text-decoration: underline;">Bartle Types</span></a>. I believe this is because the Bartle Types are a functional model of human personality in a game playing context. In other words, the Bartle typology works because it's a subset of a more general personality model that works.</p>
                <div class="hide-phone">
                    <div class="module_right right bottom30">
