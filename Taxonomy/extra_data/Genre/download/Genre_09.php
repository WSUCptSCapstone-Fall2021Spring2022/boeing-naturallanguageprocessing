<p>External   Structure</p>
</td>
<td width="625">
<p>wants   the security of possessions obtained by following the rules</p>
</td>
</tr>
<tr>
<td width="110">
<p><strong>Rational</strong></p>
</td>
<td width="183">
<p>Internal   Structure</p>
</td>
